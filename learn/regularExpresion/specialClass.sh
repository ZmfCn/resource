# [[:digit:]] ---- equals to [0-9]
# [[:lower:]] ---- equals to [a-z]
# [[:upper:]] ---- equals to [A-Z]
# [[:print:]] ---- matches any characters able to be printed
# [[:punct:]] ---- matches any punctuations
# [[:space:]] ---- matches any blank characters: space, table, NL, FF, VT, CR
